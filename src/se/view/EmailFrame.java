    public void sendMessage(String user){
        if(user.equals("user")){
        int idMessage = 0;
        
        String email = fromField.getText();
        String message = messageTextArea.getText();
        String theDate = dateLabel.getText();
  
        if ("".equals(fromField.getText())) {
            JOptionPane.showMessageDialog(null, "Du beh√∂ver ange din mailadress");
            } else if (!fromField.getText().contains("@")) {
                JOptionPane.showMessageDialog(null, "Kontrollera din mailadress! ");
            } else if (!fromField.getText().contains(".")) {
                JOptionPane.showMessageDialog(null, "Kontrollera din mailadress! ");
            } else if (messageTextArea.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Skriv in ditt meddelande!");
            } else {
                query = "INSERT INTO `Message`(`Email`, `Message`, `Date`) VALUES (?,?,?)";

                PreparedStatement ps;

                try {
                    ps = MyConnection.getConnection().prepareStatement(query, Statement.RETURN_GENERATED_KEYS);
                    ps.setString(1, email);
                    ps.setString(2, message);
                    ps.setString(3, theDate);

                    int update = ps.executeUpdate();
                    if (update == 1) {
                        rs = ps.getGeneratedKeys();
                        if (rs.next()) {
                            idMessage = rs.getInt(1);
                            JOptionPane.showMessageDialog(null, "Tack! Vi har tagit emot ditt meddelande!");
                            fromField.setText("");
                            messageTextArea.setText("");
                            this.setVisible(false);
                        }
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(EmailFrame.class.getName()).log(Level.SEVERE, null, ex);
                } finally {
                    try {
                        if (rs != null) {
                            rs.close();
                        }
                    } catch (SQLException e) {
                        System.out.println(e.getMessage());
                    }
                }

            }
        } else {
            if (messageTextArea.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Skriv in ditt meddelande!");
            } else {
                JOptionPane.showMessageDialog(null, "Tack! Ditt meddelande har skickats!");
                this.setVisible(false);
                AdminFrame ad = new AdminFrame();
                ad.setVisible(true);
            }
        }
    }

